
public class GUIRei13CircleData {

	
	public GUIRei13CircleData(int x, int y, int r, int colorR, int colorG, int colorB) {
		super();
		X = x;
		Y = y;
		R = r;
		ColorR = colorR;
		ColorG = colorG;
		ColorB = colorB;
	}
	private int X;
	public int getX() {
		return X;
	}
	public void setX(int x) {
		X = x;
	}
	public int getY() {
		return Y;
	}
	public void setY(int y) {
		Y = y;
	}
	public int getR() {
		return R;
	}
	public void setR(int r) {
		R = r;
	}
	public int getColorR() {
		return ColorR;
	}
	public void setColorR(int colorR) {
		ColorR = colorR;
	}
	public int getColorG() {
		return ColorG;
	}
	public void setColorG(int colorG) {
		ColorG = colorG;
	}
	public int getColorB() {
		return ColorB;
	}
	public void setColorB(int colorB) {
		ColorB = colorB;
	}
	private int Y;
	private int R;
	private int ColorR;
	private int ColorG;
	private int ColorB;
	
	
	
}
